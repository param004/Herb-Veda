export const getCityStateFromPincode = (pincode) => {
  if (!pincode || pincode.length !== 6) {
    return null;
  }
  
  return pincodeData[pincode] || null;
};

export const isValidIndianPincode = (pincode) => {
  // Indian pincode format: 6 digits
  const pincodeRegex = /^[1-9][0-9]{5}$/;
  return pincodeRegex.test(pincode);
};

export const formatIndianPhoneNumber = (phone) => {
  // Remove all non-digits
  const digits = phone.replace(/\D/g, '');
  
  // If it's already 10 digits and looks like Indian number, add +91
  if (digits.length === 10 && /^[6-9]/.test(digits)) {
    return `+91 ${digits}`;
  }
  
  // If it already starts with 91 and has 12 digits total
  if (digits.length === 12 && digits.startsWith('91')) {
    return `+91 ${digits.slice(2)}`;
  }
  
  // If it already has country code, return as is
  if (digits.length > 10) {
    return phone;
  }
  
  return phone;
};